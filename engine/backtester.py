@dataclass
class Signal:
    """A trading signal generated by a strategy."""
    timestamp: int  # unix ms
    side: str  # "long" or "short"
    strength: float = 1.0  # 0-1, used for position sizing
    strategy: str = ""
    reason: str = ""
    stop_loss: Optional[float] = None
    take_profit: Optional[float] = None
    time_stop_hours: Optional[int] = None
    metadata: dict = field(default_factory=dict)


@dataclass
class Trade:
    """A completed trade."""
    entry_time: int
    exit_time: int
    side: str
    entry_price: float
    exit_price: float
    size: float  # in quote currency
    pnl_gross: float
    commission: float
    slippage: float
    pnl_net: float
    exit_reason: str = ""  # "take_profit", "stop_loss", "time_stop", "signal_exit"
    strategy: str = ""

    def to_dict(self) -> dict:
        return {
            "entry_time": self.entry_time,
            "exit_time": self.exit_time,
            "side": self.side,
            "entry_price": self.entry_price,
            "exit_price": self.exit_price,
            "size": self.size,
            "pnl_gross": self.pnl_gross,
            "commission": self.commission,
            "slippage": self.slippage,
            "pnl_net": self.pnl_net,
            "exit_reason": self.exit_reason,
            "strategy": self.strategy,
        }


