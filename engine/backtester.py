# WALK-FORWARD VALIDATOR
# ═══════════════════════════════════════════════════════════════

class WalkForwardValidator:
    """
    Walk-forward validation for time series strategies.
    Splits data into rolling train/val/test windows.
    """

    def __init__(
        self,
        config: BacktestConfig,
        risk_config: RiskConfig,
        train_days: int = 60,
        val_days: int = 15,
        test_days: int = 15,
    ):
        self.backtester = Backtester(config, risk_config)
        self.train_days = train_days
        self.val_days = val_days
        self.test_days = test_days

    def _split_by_time(
        self, df: pd.DataFrame
    ) -> list[tuple[pd.DataFrame, pd.DataFrame, pd.DataFrame]]:
        """
        Create walk-forward folds.
        Each fold: [train_window | val_window | test_window]
        Windows slide forward by test_window size.
        """
        tf_ms = df["timestamp"].diff().median()
        candles_per_day = 86_400_000 / tf_ms

        train_bars = int(self.train_days * candles_per_day)
        val_bars = int(self.val_days * candles_per_day)
        test_bars = int(self.test_days * candles_per_day)
        fold_size = train_bars + val_bars + test_bars
        step_size = test_bars

        folds = []
        start = 0
        while start + fold_size <= len(df):
            train = df.iloc[start: start + train_bars].copy().reset_index(drop=True)
            val = df.iloc[start + train_bars: start + train_bars + val_bars].copy().reset_index(drop=True)
            test = df.iloc[start + train_bars + val_bars: start + fold_size].copy().reset_index(drop=True)
            folds.append((train, val, test))
            start += step_size

        return folds

    def validate(
        self,
        df: pd.DataFrame,
        strategy_fn: Callable,
        strategy_name: str = "unnamed",
    ) -> dict:
        """
        Run full walk-forward validation.

        Returns dict with:
            - fold_results: list of per-fold metrics
            - aggregate: aggregated metrics
            - is_robust: whether strategy passes robustness checks
            - oos_degradation: average OOS degradation
        """
        folds = self._split_by_time(df)
        if not folds:
            logger.warning("Not enough data for walk-forward validation")
            return {
                "fold_results": [],
                "aggregate": StrategyMetrics(),
                "is_robust": False,
                "oos_degradation": 100.0,
            }

        fold_results = []
        oos_degradations = []

        for i, (train, val, test) in enumerate(folds):
            logger.info(f"Walk-forward fold {i + 1}/{len(folds)}")

            # Run on train (in-sample)
            _, _, train_metrics = self.backtester.run(train, strategy_fn, f"{strategy_name}_train_f{i}")
            train_metrics.dataset_label = "train"

            # Run on val (validation / parameter selection)
            _, _, val_metrics = self.backtester.run(val, strategy_fn, f"{strategy_name}_val_f{i}")
            val_metrics.dataset_label = "val"

            # Run on test (true out-of-sample)
            trades, eq, test_metrics = self.backtester.run(test, strategy_fn, f"{strategy_name}_test_f{i}")
            test_metrics.dataset_label = "test"

            # OOS degradation
            degradation = compute_oos_degradation(train_metrics, test_metrics)
            oos_degradations.append(degradation)
            test_metrics.oos_degradation_pct = degradation
            test_metrics.oos_sharpe = test_metrics.sharpe_ratio

            fold_results.append({
                "fold": i,
                "train": train_metrics,
                "val": val_metrics,
                "test": test_metrics,
            })

        # Aggregate OOS results
        oos_metrics = [f["test"] for f in fold_results]
        aggregate = compute_walk_forward_metrics(oos_metrics)
        aggregate.oos_degradation_pct = float(np.mean(oos_degradations))

        # Robustness check
        is_robust = (
            aggregate.sharpe_ratio >= self.backtester.config.min_sharpe * 0.8
            and aggregate.max_drawdown_pct <= self.backtester.config.max_drawdown_pct
            and aggregate.total_trades >= self.backtester.config.min_trades
            and aggregate.oos_degradation_pct <= self.backtester.config.max_oos_degradation_pct
            and aggregate.profit_factor > 1.0
        )

        return {
            "fold_results": fold_results,
            "aggregate": aggregate,
            "is_robust": is_robust,
            "oos_degradation": float(np.mean(oos_degradations)),
        }
