@dataclass
class Position:
    """An open position."""
    entry_time: int
    side: str
    entry_price: float
    size: float
    stop_loss: Optional[float] = None
    take_profit: Optional[float] = None
    time_stop_hours: int = 48
    strategy: str = ""


