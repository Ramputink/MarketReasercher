class Backtester:
    """
    Event-driven backtester with realistic cost modeling.

    Strategy interface:
        strategy_fn(df, bar_idx, open_position) -> Optional[Signal]

    The strategy receives the FULL DataFrame + current bar index.
    Strategies MUST only access df.iloc[:bar_idx+1] — no future leakage
    is enforced by convention (strategies use bar_idx as upper bound).
    This avoids creating O(n) DataFrame slices per bar.
    """

    def __init__(
        self,
        config: BacktestConfig,
        risk_config: RiskConfig,
    ):
        self.config = config
        self.risk_config = risk_config

    def _apply_slippage(self, price: float, side: str, is_entry: bool) -> float:
        """Apply slippage: adverse direction for entries, adverse for exits."""
        slip_pct = self.config.slippage_bps / 10000
        if (side == "long" and is_entry) or (side == "short" and not is_entry):
            return price * (1 + slip_pct)
        else:
            return price * (1 - slip_pct)

    def _compute_commission(self, notional: float) -> float:
        return notional * self.config.commission_rate

    def _compute_position_size(
        self,
        equity: float,
        price: float,
        atr_value: float,
        signal_strength: float = 1.0,
    ) -> float:
        """Compute position size based on risk config."""
        if self.risk_config.sizing_method == "fixed_pct":
            return equity * self.config.max_position_pct * signal_strength

        elif self.risk_config.sizing_method == "volatility_scaled":
            if atr_value <= 0 or price <= 0:
                return equity * self.config.max_position_pct * 0.5
            # Scale position so that a 1-ATR move = target vol fraction of equity
            daily_vol_pct = atr_value / price
            annual_vol = daily_vol_pct * np.sqrt(365)
            target_size = equity * (self.risk_config.vol_target_annualized / max(annual_vol, 0.01))
            return min(target_size, equity * self.config.max_position_pct) * signal_strength

        elif self.risk_config.sizing_method == "kelly":
            return equity * self.risk_config.kelly_fraction * self.config.max_position_pct * signal_strength

        return equity * self.config.max_position_pct * signal_strength

    def run(
        self,
        df: pd.DataFrame,
        strategy_fn: Callable,
        strategy_name: str = "unnamed",
    ) -> tuple[pd.DataFrame, pd.Series, StrategyMetrics]:
        """
        Run backtest on a single data segment.

        Args:
            df: OHLCV + features DataFrame
            strategy_fn: function(df_up_to_bar, bar_idx, position) -> Optional[Signal]
            strategy_name: name for logging

        Returns:
            (trades_df, equity_curve, metrics)
        """
        equity = self.config.initial_capital
        equity_curve = []
        trades = []
        position: Optional[Position] = None

        # Need ATR for position sizing
        has_atr = "atr_14" in df.columns

        # Warm-up period: skip first 50 bars
        warmup = 50

        for i in range(warmup, len(df)):
            current_bar = df.iloc[i]
            timestamp = int(current_bar["timestamp"])
            price = float(current_bar["close"])

            # ─── Check exits for open position ───
            if position is not None:
                exit_signal = False
                exit_reason = ""
                exit_price = price

                # Stop loss
                if position.stop_loss is not None:
                    if position.side == "long" and float(current_bar["low"]) <= position.stop_loss:
                        exit_price = position.stop_loss
                        exit_signal = True
                        exit_reason = "stop_loss"
                    elif position.side == "short" and float(current_bar["high"]) >= position.stop_loss:
                        exit_price = position.stop_loss
                        exit_signal = True
                        exit_reason = "stop_loss"

                # Take profit
                if not exit_signal and position.take_profit is not None:
                    if position.side == "long" and float(current_bar["high"]) >= position.take_profit:
                        exit_price = position.take_profit
                        exit_signal = True
                        exit_reason = "take_profit"
                    elif position.side == "short" and float(current_bar["low"]) <= position.take_profit:
                        exit_price = position.take_profit
                        exit_signal = True
                        exit_reason = "take_profit"

                # Time stop
                if not exit_signal:
                    hours_held = (timestamp - position.entry_time) / 3_600_000
                    if hours_held >= position.time_stop_hours:
                        exit_signal = True
                        exit_reason = "time_stop"

                # Strategy exit signal
                if not exit_signal:
                    # Pass full df + bar_idx (strategies enforce no-leakage via bar_idx)
                    signal = strategy_fn(df, i, position)
                    if signal is not None and signal.side != position.side:
                        exit_signal = True
                        exit_reason = "signal_exit"

                if exit_signal:
                    # Execute exit
                    exit_price_slipped = self._apply_slippage(exit_price, position.side, False)
                    commission = self._compute_commission(position.size)
                    slippage_cost = abs(exit_price - exit_price_slipped) * (position.size / exit_price)

                    if position.side == "long":
                        pnl_gross = (exit_price_slipped - position.entry_price) / position.entry_price * position.size
                    else:
                        pnl_gross = (position.entry_price - exit_price_slipped) / position.entry_price * position.size

                    pnl_net = pnl_gross - commission - slippage_cost

                    trade = Trade(
                        entry_time=position.entry_time,
                        exit_time=timestamp,
                        side=position.side,
                        entry_price=position.entry_price,
                        exit_price=exit_price_slipped,
                        size=position.size,
                        pnl_gross=pnl_gross,
                        commission=commission,
                        slippage=slippage_cost,
                        pnl_net=pnl_net,
                        exit_reason=exit_reason,
                        strategy=strategy_name,
                    )
                    trades.append(trade)
                    equity += pnl_net
                    position = None

            # ─── Check for new entry ───
            if position is None:
                signal = strategy_fn(df, i, None)

                if signal is not None:
                    atr_val = float(current_bar.get("atr_14", price * 0.02)) if has_atr else price * 0.02
                    size = self._compute_position_size(equity, price, atr_val, signal.strength)

                    entry_price = self._apply_slippage(price, signal.side, True)
                    entry_commission = self._compute_commission(size)
                    equity -= entry_commission  # pay commission on entry

                    # Set stop/take profit
                    sl = signal.stop_loss
                    tp = signal.take_profit
                    if sl is None:
                        if signal.side == "long":
                            sl = entry_price - self.risk_config.stop_loss_atr_mult * atr_val
                        else:
                            sl = entry_price + self.risk_config.stop_loss_atr_mult * atr_val
                    if tp is None:
                        if signal.side == "long":
                            tp = entry_price + self.risk_config.take_profit_atr_mult * atr_val
                        else:
                            tp = entry_price - self.risk_config.take_profit_atr_mult * atr_val

                    position = Position(
                        entry_time=timestamp,
                        side=signal.side,
                        entry_price=entry_price,
                        size=size,
                        stop_loss=sl,
                        take_profit=tp,
                        time_stop_hours=signal.time_stop_hours or self.risk_config.time_stop_hours,
                        strategy=strategy_name,
                    )

            # Record equity
            # Mark-to-market if position open
            mtm_equity = equity
            if position is not None:
                if position.side == "long":
                    unrealized = (price - position.entry_price) / position.entry_price * position.size
                else:
                    unrealized = (position.entry_price - price) / position.entry_price * position.size
                mtm_equity += unrealized

            equity_curve.append({"timestamp": timestamp, "equity": mtm_equity})

        # Close any remaining position at last bar
        if position is not None:
            last_price = float(df.iloc[-1]["close"])
            exit_price_slipped = self._apply_slippage(last_price, position.side, False)
            commission = self._compute_commission(position.size)
            slippage_cost = abs(last_price - exit_price_slipped) * (position.size / last_price)
            if position.side == "long":
                pnl_gross = (exit_price_slipped - position.entry_price) / position.entry_price * position.size
            else:
                pnl_gross = (position.entry_price - exit_price_slipped) / position.entry_price * position.size
            pnl_net = pnl_gross - commission - slippage_cost
            trades.append(Trade(
                entry_time=position.entry_time,
                exit_time=int(df.iloc[-1]["timestamp"]),
                side=position.side,
                entry_price=position.entry_price,
                exit_price=exit_price_slipped,
                size=position.size,
                pnl_gross=pnl_gross,
                commission=commission,
                slippage=slippage_cost,
                pnl_net=pnl_net,
                exit_reason="end_of_data",
                strategy=strategy_name,
            ))

        # Build results
        trades_df = pd.DataFrame([t.to_dict() for t in trades]) if trades else pd.DataFrame()
        eq_df = pd.DataFrame(equity_curve)
        eq_series = pd.Series(
            eq_df["equity"].values,
            index=pd.to_datetime(eq_df["timestamp"], unit="ms"),
        ) if len(eq_df) > 0 else pd.Series(dtype=float)

        metrics = compute_metrics(
            trades_df, eq_series,
            initial_capital=self.config.initial_capital,
            strategy_name=strategy_name,
        )

        return trades_df, eq_series, metrics


